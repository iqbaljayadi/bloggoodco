    <p><strong>Attention Grabber is designed to make sure all your visitors know about the things most important to <em>you</em>.</strong></p>

    <p><strong>Grab your visitor&#8217;s attention and get them to sign up for your email list, or tell them about a specific page they should visit.</strong></p>

    <p>This is a free alternative to <a href="http://www.hellobar.com/">HelloBar</a>, a SaaS which charges based on the number of clicks the bar receives. If your bar receives 7,500 clicks per month, HelloBar would cost you $50/month.</p>

    <p>Little bars like these are cropping up all over the Web (e.g., at Neil Patel&#8217;s <a href="http://www.quicksprout.com">QuickSprout</a>), and it&#8217;s no wonder why&#8212;they do a fantastic job of getting your visitors to complete your goals for them.</p>

    <h2 id="features">Features</h2>

    <p>The Attention Grabber features the ability to:</p>

    <ul>
        <li>Sign up email addresses to your <a href="http://customer.io/" title="Customer.io email sending service">Customer.io</a> account,</li>
        <li>Track the number of signups or clicks you receive using Google Analytics,</li>
        <li>Customize the look and feel of the bar (background color, button color, etc.), and</li>
        <li>Responsively show or hide content to fit comfortable at the top of the user&#8217;s screen.</li>
    </ul>

